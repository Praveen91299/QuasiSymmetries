def write_csv(path: Path, rows: list[dict]) -> None:
    if not rows:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames: list[str] = []
    for row in rows:
        for key in row:
            if key not in fieldnames:
                fieldnames.append(key)
    with path.open("w", newline="") as file_obj:
        writer = csv.DictWriter(file_obj, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def jsonable(value):
    if isinstance(value, np.ndarray):
        return jsonable(value.tolist())
    if isinstance(value, complex):
        return {"real": float(value.real), "imag": float(value.imag)}
    if isinstance(value, np.generic):
        return jsonable(value.item())
    if isinstance(value, dict):
        return {str(key): jsonable(val) for key, val in value.items()}
    if isinstance(value, (list, tuple)):
        return [jsonable(val) for val in value]
    return value


def save_json(path: Path, payload) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w") as file_obj:
        json.dump(jsonable(payload), file_obj, indent=2, allow_nan=False)
        file_obj.write("\n")


def sym_strings(symmetries) -> list[str]:
    return [str(sym) for sym in symmetries]


def write_entropies(file_obj, label: str, entropies) -> None:
    print(label, file=file_obj)
    for i, ent in enumerate(entropies):
        print(f"  {i + 1}|{i + 2}: {ent}", file=file_obj)


def singleton_z_index(operator) -> int | None:
    """Return q for a single +Zq Pauli, otherwise ``None``."""
    if len(operator.terms) != 1:
        return None
    (term, coeff), = operator.terms.items()
    if abs(complex(coeff) - 1.0) > 1e-9:
        return None
    if len(term) != 1:
        return None
    qubit, pauli = term[0]
    if pauli != "Z":
        return None
    return int(qubit)


def original_symmetries_in_fiedler_order(original_symmetries, transformed_symmetries, ordering):
    """Order original generators by the Fiedler order of their Clifford Z targets.

    With ``generator_mapping='positive_z'`` and a full-rank symmetry list,
    generator ``i`` maps to a single ``Zq``.  If the Fiedler ordering starts
    with old qubit ``q``, the meaningful symmetry to print first is the
    original generator that mapped to ``Zq``.
    """
    target_to_original = {}
    for original, transformed in zip(original_symmetries, transformed_symmetries):
        target = singleton_z_index(transformed)
        if target is not None:
            target_to_original[target] = original

    if len(target_to_original) != len(original_symmetries):
        # Fallback for non-positive-Z / row-reduced conventions where the
        # transformed generators are not one-to-one single-Z targets.
        return list(original_symmetries)

    return [target_to_original[q] for q in ordering if q in target_to_original]


def load_hamiltonian_data(ham_dir: Path, system: str):
    with (ham_dir / f"{system}.pkl").open("rb") as file_obj:
        return pickle.load(file_obj)


def dataset_table(datasets: list[BenchmarkData], systems: list[str], objectives: list[str]):
    expected = len(systems) * len(objectives)
    if len(datasets) < expected:
        raise ValueError(
            f"Need at least {expected} datasets for {len(systems)} systems x "
            f"{len(objectives)} objectives, found {len(datasets)}."
        )

    table = {}
    index = 0
    for system in systems:
        table[system] = {}
        for objective in objectives:
            data = datasets[index]
            index += 1
            if objective not in data.tag:
                raise ValueError(
                    "Dataset order does not match requested systems/objectives: "
                    f"expected objective {objective!r}, got tag {data.tag!r} "
                    f"at position {index - 1}."
                )
            table[system][objective] = data
    return table


def run_one(
    *,
    system: str,
    objective: str,
    dataset: BenchmarkData,
    HQ,
    fci_e,
    fci_gs,
    cisd_gs,
    n_qubits: int,
    args,
    text_path: Path,
):
    transform_t0 = perf_counter()
    fci_ent_clifford, H_clifford, clifford, fci_rot = (
        get_permuted_bipartite_entanglement(
            dataset.symmetries,
            HQ,
            n_qubits,
            fci_energy=fci_e,
            fci_gs=fci_gs,
            verbose=False,
            return_state=True,
            return_clifford=True,
            log_base=args.log_base,
            use_dmrg=False,
            synthesis_basis=args.synthesis_basis,
            generator_mapping=args.generator_mapping,
        )
    )
    cisd_rot = clifford.transform_state(cisd_gs)
    cisd_ent_clifford = get_entropies_at_cuts(
        cisd_rot,
        n_qubits,
        log_base=args.log_base,
    )
    transform_seconds = perf_counter() - transform_t0

    fiedler_state = cisd_rot if args.fiedler_reference == "cisd" else fci_rot
    fiedler_t0 = perf_counter()
    reference_ent_fiedler, H_fiedler, reference_state_fiedler, fiedler_info = (
        do_fiedler_reordering(
            H_clifford,
            fiedler_state,
            n_qubits=n_qubits,
            verbose=False,
            log_base=args.log_base,
        )
    )
    fiedler_seconds = perf_counter() - fiedler_t0

    fci_state_fiedler = reorder_statevector_axes(
        fci_rot,
        fiedler_info["ordering"],
        n_qubits,
    )
    cisd_state_fiedler = (
        reference_state_fiedler
        if args.fiedler_reference == "cisd"
        else reorder_statevector_axes(cisd_rot, fiedler_info["ordering"], n_qubits)
    )
    fci_ent_fiedler = get_entropies_at_cuts(
        fci_state_fiedler,
        n_qubits,
        log_base=args.log_base,
    )
    cisd_ent_fiedler = (
        reference_ent_fiedler
        if args.fiedler_reference == "cisd"
        else get_entropies_at_cuts(cisd_state_fiedler, n_qubits, log_base=args.log_base)
    )

    perm = invert_ordering(fiedler_info["ordering"])
    transformed_symmetries = list(clifford.transformed_symmetries)
    transformed_symmetries_after_fiedler = [
        permute_qubits_in_qubit_operator(sym, perm)
        for sym in transformed_symmetries
    ]
    original_symmetries_fiedler_order = original_symmetries_in_fiedler_order(
        dataset.symmetries,
        transformed_symmetries,
        fiedler_info["ordering"],
    )

    if args.skip_dmrg:
        dmrg_bd = ""
        dmrg_energy = ""
        dmrg_seconds = 0.0
    else:
        import quimb.tensor as qtn
        from quasisymmetries.tn import find_dmrg_conv_bd_quimb

        dmrg_t0 = perf_counter()
        guess_mps = qtn.MatrixProductState.from_dense(
            fci_state_fiedler,
            cutoff=args.compress_cutoff,
        )
        dmrg_bd, dmrg_energy = find_dmrg_conv_bd_quimb(
            H_fiedler,
            n_qubits,
            fci_e,
            tol=args.dmrg_tol,
            n_sweeps=args.n_sweeps,
            reps=1,
            verbose=False,
            compress_cutoff=args.compress_cutoff,
            sweep_tol=args.sweep_tol,
            noise=args.noise,
            bsz=args.bsz,
            guess_mps=guess_mps,
            seed=args.seed,
        )
        dmrg_seconds = perf_counter() - dmrg_t0

    result = {
        "system": system,
        "objective": objective,
        "dataset_tag": dataset.tag,
        "n_qubits": n_qubits,
        "fiedler_reference": args.fiedler_reference,
        "dmrg_bd_before_fiedler": dataset.dmrg_bd,
        "dmrg_bd_after_fiedler": dmrg_bd,
        "dmrg_energy_after_fiedler": dmrg_energy,
        "transform_seconds": transform_seconds,
        "fiedler_seconds": fiedler_seconds,
        "dmrg_seconds": dmrg_seconds,
        "original_symmetries": sym_strings(dataset.symmetries),
        "symmetries_after_clifford": sym_strings(transformed_symmetries),
        "original_symmetries_in_fiedler_order": sym_strings(
            original_symmetries_fiedler_order
        ),
        "transformed_symmetries_after_fiedler": sym_strings(
            transformed_symmetries_after_fiedler
        ),
        "fiedler_ordering": fiedler_info["ordering"],
        "fiedler_info": fiedler_info,
        "fci_entanglement_after_clifford": fci_ent_clifford,
        "cisd_entanglement_after_clifford": cisd_ent_clifford,
        "fci_entanglement_after_fiedler": fci_ent_fiedler,
        "cisd_entanglement_after_fiedler": cisd_ent_fiedler,
        "non_commuting_l1": dataset.non_commuting_l1,
        "num_commuting_terms": dataset.num_commuting_terms,
    }

    with text_path.open("a") as file_obj:
        print("\n" + "-" * 80, file=file_obj)
        print(f"{system} | {objective}", file=file_obj)
        print(f"Dataset tag: {dataset.tag}", file=file_obj)
        print(f"Fiedler reference: {args.fiedler_reference}", file=file_obj)
        print(f"DMRG BD before Fiedler: {dataset.dmrg_bd}", file=file_obj)
        print(f"DMRG BD after Fiedler: {dmrg_bd}", file=file_obj)
        print(f"DMRG energy after Fiedler: {dmrg_energy}", file=file_obj)
        print(f"Transform seconds: {transform_seconds:.6f}", file=file_obj)
        print(f"Fiedler seconds: {fiedler_seconds:.6f}", file=file_obj)
        print(f"DMRG seconds: {dmrg_seconds:.6f}", file=file_obj)
        print("Fiedler ordering:", fiedler_info["ordering"], file=file_obj)
        print("Original symmetries:", file=file_obj)
        for sym in dataset.symmetries:
            print(f"  {sym}", file=file_obj)
        print("Symmetries after Clifford:", file=file_obj)
        for sym in transformed_symmetries:
            print(f"  {sym}", file=file_obj)
        print("Original symmetries in Fiedler order:", file=file_obj)
        for sym in original_symmetries_fiedler_order:
            print(f"  {sym}", file=file_obj)
        print("Transformed single-Z symmetries after Clifford + Fiedler:", file=file_obj)
        for sym in transformed_symmetries_after_fiedler:
            print(f"  {sym}", file=file_obj)
        write_entropies(file_obj, "FCI entanglement after Clifford:", fci_ent_clifford)
        write_entropies(file_obj, "CISD entanglement after Clifford:", cisd_ent_clifford)
        write_entropies(file_obj, "FCI entanglement after Clifford + Fiedler:", fci_ent_fiedler)
        write_entropies(file_obj, "CISD entanglement after Clifford + Fiedler:", cisd_ent_fiedler)

    return result


def parse_args():
    parser = argparse.ArgumentParser(
        description="Run Fiedler DMRG benchmarks for JUL09 state-agnostic BS(n) symmetries."
    )
    parser.add_argument("--ham-dir", type=Path, default=ROOT / "saved" / "hamiltonians")
    parser.add_argument(
        "--input-datasets",
        type=Path,
        default=ROOT / "saved" / "results" / "JUL09" / "state_agnostic_bs_benchmark_datasets",
    )
    parser.add_argument("--output-dir", type=Path, default=ROOT / "saved" / "results" / "JUL09")
    parser.add_argument("--systems", nargs="+", default=SYSTEMS)
    parser.add_argument("--objectives", nargs="+", choices=OBJECTIVES, default=OBJECTIVES)
    parser.add_argument("--fiedler-reference", choices=["cisd", "fci"], default="cisd")
    parser.add_argument("--skip-dmrg", action="store_true")
    parser.add_argument("--synthesis-basis", choices=["X", "Z"], default="Z")
    parser.add_argument(
        "--generator-mapping",
        choices=["row_reduced", "positive_z"],
        default="positive_z",
    )
    parser.add_argument("--log-base", type=float, default=float(np.e))
    parser.add_argument("--dmrg-tol", type=float, default=1.6e-3)
    parser.add_argument("--n-sweeps", type=int, default=100)
    parser.add_argument("--compress-cutoff", type=float, default=1e-20)
    parser.add_argument("--sweep-tol", type=float, default=1e-6)
    parser.add_argument("--noise", type=float, default=1e0)
    parser.add_argument("--bsz", type=int, default=2)
    parser.add_argument("--seed", type=int, default=0)
    return parser.parse_args()


def main():
    args = parse_args()
    args.output_dir.mkdir(parents=True, exist_ok=True)

    text_path = args.output_dir / "state_agnostic_bs_fiedler_benchmarks.txt"
    metrics_path = args.output_dir / "state_agnostic_bs_fiedler_metrics.json"
    csv_path = args.output_dir / "state_agnostic_bs_fiedler_dmrg_bond_dimensions.csv"

    datasets = BenchmarkData.load_datasets(args.input_datasets)
    table = dataset_table(datasets, args.systems, args.objectives)

    all_results = {
        "input_datasets": str(args.input_datasets),
        "fiedler_reference": args.fiedler_reference,
        "synthesis_basis": args.synthesis_basis,
        "generator_mapping": args.generator_mapping,
        "systems": {},
    }
    rows = []

    with text_path.open("w") as file_obj:
        print("State-agnostic BS(n) + Fiedler benchmarks", file=file_obj)
        print(f"Input datasets: {args.input_datasets}", file=file_obj)
        print(f"Systems: {args.systems}", file=file_obj)
        print(f"Objectives: {args.objectives}", file=file_obj)
        print(f"Fiedler reference: {args.fiedler_reference}", file=file_obj)
        print(f"DMRG skipped: {args.skip_dmrg}", file=file_obj)
        print(f"Clifford synthesis basis: {args.synthesis_basis}", file=file_obj)
        print(f"Clifford generator mapping: {args.generator_mapping}", file=file_obj)

    for system in args.systems:
        print(f"Starting system: {system}", flush=True)
        H, fci_e, fci_gs, cisd_e, cisd_gs = load_hamiltonian_data(args.ham_dir, system)
        HQ = jordan_wigner(H)
        n_qubits = count_qubits(HQ)

        all_results["systems"][system] = {
            "n_qubits": n_qubits,
            "fci_energy": fci_e,
            "cisd_energy": cisd_e,
            "objectives": {},
        }

        row = {"system": system}
        for objective in args.objectives:
            result = run_one(
                system=system,
                objective=objective,
                dataset=table[system][objective],
                HQ=HQ,
                fci_e=fci_e,
                fci_gs=fci_gs,
                cisd_gs=cisd_gs,
                n_qubits=n_qubits,
                args=args,
                text_path=text_path,
            )
            all_results["systems"][system]["objectives"][objective] = result
            row[f"{objective}_before_fiedler"] = result["dmrg_bd_before_fiedler"]
            row[f"{objective}_after_fiedler"] = result["dmrg_bd_after_fiedler"]
            row[f"{objective}_dmrg_energy_after_fiedler"] = result[
                "dmrg_energy_after_fiedler"
            ]
            save_json(metrics_path, all_results)

        rows.append(row)
        write_csv(csv_path, rows)

    save_json(metrics_path, all_results)
    write_csv(csv_path, rows)
    print(f"Done. Wrote results to {args.output_dir}")


if __name__ == "__main__":
    main()
