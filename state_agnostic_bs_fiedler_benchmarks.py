"""Run Fiedler-reordered DMRG benchmarks for JUL09 state-agnostic BS(n) data.

The input symmetries are loaded from
``saved/results/JUL09/state_agnostic_bs_benchmark_datasets.json`` generated by
``state_agnostic_bs_benchmarks.py``.  For each system/objective pair, this
script:

1. Clifford-transforms the Hamiltonian and FCI/CISD states.
2. Computes a Fiedler qubit ordering from the transformed reference state
   (CISD by default, matching the JUL08 convention).
3. Applies that ordering to the transformed Hamiltonian and FCI state.
4. Runs the same quimb DMRG convergence settings used by ``BenchmarkData``.

Outputs:

- ``state_agnostic_bs_fiedler_benchmarks.txt``
- ``state_agnostic_bs_fiedler_metrics.json``
- ``state_agnostic_bs_fiedler_dmrg_bond_dimensions.csv``
"""

