"""Run JUL08 Clifford/Fiedler benchmarks with the Z, positive-Z convention.

Most symmetry generators are loaded from the MAY27 benchmark files.  The
BLISS+HCT(n) generators are regenerated by running Pauli BLISS first and then
HCT on the BLISS Hamiltonian, matching ``bliss_hct_benchmark.py``.  Fiedler
orderings are computed from the transformed CISD state, not the FCI state.
"""

from __future__ import annotations

import argparse
import csv
import json
import pickle
from pathlib import Path

import numpy as np
import pandas as pd
import quimb.tensor as qtn
from openfermion import MolecularData, count_qubits, get_sparse_operator, jordan_wigner

from quasisymmetries.benchmark import BenchmarkData
from quasisymmetries.bliss import lp_bliss_paper_real_pauli_1norm
from quasisymmetries.clifford_symmetry_optimized import (
    permute_qubits_in_qubit_operator,
)
from quasisymmetries.fiedler import (
    do_fiedler_reordering,
    invert_ordering,
    reorder_statevector_axes,
)
from quasisymmetries.metrics import (
    comm_sq_exp_fast,
    get_entropies_at_cuts,
    get_permuted_bipartite_entanglement,
)
from quasisymmetries.sym import hct_mod
from quasisymmetries.tn import MPO_from_QubitOperator, find_dmrg_conv_bd_quimb


ham_dir = Path("./saved/hamiltonians")
dataset_dir = Path("./saved/results/MAY27")
output_dir = Path("./saved/results/JUL08")

synthesis_basis = "Z"
generator_mapping = "positive_z"
log_base = np.e
bliss_hct_tag = "Pauli BLISS+HCT N Comm"

systems = [
    "H4chain_eqm",
    "H4chain_corr",
    "H4chain_diss",
    "H4rect_corr",
    "H4rect_diss",
    "LIH_eqm",
    "LIH_corr",
    "H2O_eqm",
    "H2O_corr",
    "H2O_diss",
    "N2frozen_eqm",
    "N2frozen_corr",
    "N2frozen_diss",
]

dmrg_jobs = [
    ("Seniority", "SEN N/2 Comm", False),
    ("HCT N/2", "HCT N/2 Comm", False),
    ("HCT N", "HCT N Comm", False),
    ("HCT+BLISS N", bliss_hct_tag, False),
    ("BS N/2", "BS N/2 Comm", False),
    ("BS N", "BS N Comm", False),
    ("BS N + Fiedler", "BS N Comm", True),
]

mpo_jobs = [
    ("HCT N", "HCT N Comm", False),
    ("BS N", "BS N Comm", False),
    ("HCT N + Fiedler", "HCT N Comm", True),
    ("BS N + Fiedler", "BS N Comm", True),
]


def runtime_factors(n_s, d, chi_mps, chi_mpo):
    t1 = n_s * chi_mpo * (chi_mps**3) * (d**2)
    t2 = n_s * (chi_mpo**2) * (chi_mps**2) * (d**3)
    return max(t1, t2), (t1, t2)


def memory_factors(n_s, d, chi_mps, chi_mpo):
    m1 = n_s * chi_mpo * (chi_mps**2)
    m2 = n_s * (chi_mpo**2) * (d**2)
    return max(m1, m2), (m1, m2)


def write_csv(path, rows):
    if not rows:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    keys = []
    for row in rows:
        for key in row:
            if key not in keys:
                keys.append(key)
    with open(path, "w", newline="") as file_obj:
        writer = csv.DictWriter(file_obj, fieldnames=keys)
        writer.writeheader()
        writer.writerows(rows)


def as_jsonable(value):
    if isinstance(value, np.ndarray):
        return value.tolist()
    if isinstance(value, np.generic):
        return value.item()
    if isinstance(value, complex):
        return {"real": value.real, "imag": value.imag}
    if isinstance(value, dict):
        return {str(key): as_jsonable(val) for key, val in value.items()}
    if isinstance(value, (list, tuple)):
        return [as_jsonable(val) for val in value]
    return value


def save_json(path, data):
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as file_obj:
        json.dump(as_jsonable(data), file_obj, indent=2)
        file_obj.write("\n")


def sym_strings(symmetries):
    return [str(sym) for sym in symmetries]


def write_entropies(file_obj, label, entropies):
    print(label, file=file_obj)
    for i, ent in enumerate(entropies):
        print(f"  {i + 1}|{i + 2}: {ent}", file=file_obj)


def load_fermionic_runtime_data():
    path = Path("./saved/results/fermionic_mpo_mps_bond_dimensions_test.csv")
    if not path.exists():
        return {}, {}
    data = pd.read_csv(path)
    ferm_mps_bd = dict(zip(data["system"], data["mps_converged_bond_dimension"]))
    ferm_mpo_bd = dict(zip(data["system"], data["mpo_bond_dimension"]))
    return ferm_mps_bd, ferm_mpo_bd


def bliss_info_summary(info):
    keys = [
        "success",
        "message",
        "initial_pauli_l1",
        "final_pauli_l1",
        "pauli_l1_reduction",
        "relative_pauli_l1_reduction",
        "n_killers",
        "n_pauli_terms_initial",
        "n_pauli_terms_final",
    ]
    return {key: info[key] for key in keys if key in info}


def generate_bliss_hct_dataset(system, H, HQ, cisd_gs, n_qubits, text):
    molecule = MolecularData(filename=str(ham_dir / system))
    n_elec = molecule.n_electrons - 4 if system.startswith("N2") else molecule.n_electrons

    print("\nRunning Pauli BLISS before HCT...", file=text)
    print(f"n_electrons used for BLISS: {n_elec}", file=text)

    H_bliss, info = lp_bliss_paper_real_pauli_1norm(
        H,
        n_electrons=n_elec,
        n_orb=n_qubits,
    )
    info_summary = bliss_info_summary(info)
    print("BLISS info:", info_summary, file=text)

    HQ_bliss = jordan_wigner(H_bliss)
    Hs = get_sparse_operator(HQ, n_qubits)
    comm_sq_exp_cisd = lambda s_list: comm_sq_exp_fast(
        s_list,
        Hs,
        cisd_gs,
        n_qubits,
    )
    sym_group_score_func = lambda s_list: -comm_sq_exp_cisd(s_list)
    sym_metric_func = lambda symmetry: -sym_group_score_func([symmetry])

    sym_hct_bliss, eps_bliss = hct_mod(
        HQ_bliss,
        n_qubits,
        sym_metric_func=sym_metric_func,
        use_coeffs_eps=True,
    )
    dataset = BenchmarkData(
        tag=bliss_hct_tag,
        symmetries=sym_hct_bliss,
        clifford_synthesis_basis=synthesis_basis,
        clifford_generator_mapping=generator_mapping,
    )
    return dataset, eps_bliss, info_summary


def make_runtime_rows(dmrg_rows, mpo_rows):
    ferm_mps_bd, ferm_mpo_bd = load_fermionic_runtime_data()
    dmrg_bd = {
        (row["system"], row["benchmark"]): row["dmrg_bd"]
        for row in dmrg_rows
        if row["dmrg_bd"] != ""
    }
    mpo_bd = {
        (row["system"], row["benchmark"]): row["mpo_bd"]
        for row in mpo_rows
        if row["mpo_bd"] != ""
    }

    rows = []
    for system in systems:
        n_qubits = 8
        for row in dmrg_rows:
            if row["system"] == system:
                n_qubits = int(row["n_qubits"])
                break

        out = {"system": system}
        if system in ferm_mps_bd and system in ferm_mpo_bd:
            out["T Ferm"], _ = runtime_factors(
                n_qubits // 2,
                4,
                ferm_mps_bd[system],
                ferm_mpo_bd[system],
            )
            out["M Ferm"], _ = memory_factors(
                n_qubits // 2,
                4,
                ferm_mps_bd[system],
                ferm_mpo_bd[system],
            )

        if (system, "BS N") in dmrg_bd and (system, "BS N") in mpo_bd:
            out["T BS"], _ = runtime_factors(
                n_qubits,
                2,
                dmrg_bd[(system, "BS N")],
                mpo_bd[(system, "BS N")],
            )
            out["M BS"], _ = memory_factors(
                n_qubits,
                2,
                dmrg_bd[(system, "BS N")],
                mpo_bd[(system, "BS N")],
            )

        if (
            (system, "BS N + Fiedler") in dmrg_bd
            and (system, "BS N + Fiedler") in mpo_bd
        ):
            out["T BS + Fiedler"], _ = runtime_factors(
                n_qubits,
                2,
                dmrg_bd[(system, "BS N + Fiedler")],
                mpo_bd[(system, "BS N + Fiedler")],
            )
            out["M BS + Fiedler"], _ = memory_factors(
                n_qubits,
                2,
                dmrg_bd[(system, "BS N + Fiedler")],
                mpo_bd[(system, "BS N + Fiedler")],
            )

        rows.append(out)
    return rows


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--systems", nargs="+", default=systems)
    parser.add_argument("--skip-dmrg", action="store_true")
    parser.add_argument("--skip-mpo", action="store_true")
    args = parser.parse_args()

    output_dir.mkdir(parents=True, exist_ok=True)
    text_file = output_dir / "july08_clifford_benchmarks.txt"
    dmrg_csv = output_dir / "july08_dmrg_bond_dimensions.csv"
    mpo_csv = output_dir / "july08_mpo_bond_dimensions.csv"
    runtime_csv = output_dir / "july08_runtime_memory.csv"
    json_file = output_dir / "july08_clifford_benchmarks.json"

    dmrg_rows = []
    mpo_rows = []
    all_results = {
        "synthesis_basis": synthesis_basis,
        "generator_mapping": generator_mapping,
        "log_base": "e",
        "systems": {},
    }

    with open(text_file, "w") as text:
        print("JUL08 Clifford/Fiedler benchmarks", file=text)
        print(f"synthesis_basis = {synthesis_basis}", file=text)
        print(f"generator_mapping = {generator_mapping}", file=text)
        print("Fiedler ordering state = transformed CISD state", file=text)

        for system in args.systems:
            print(f"\nStarting {system}")
            print("\n" + "=" * 80, file=text)
            print(system, file=text)

            with open(ham_dir / f"{system}.pkl", "rb") as file_obj:
                H, fci_e, fci_gs, cisd_e, cisd_gs = pickle.load(file_obj)
            HQ = jordan_wigner(H)
            n_qubits = count_qubits(HQ)

            datasets = BenchmarkData.load_datasets(
                str(dataset_dir / f"_nc_exp_cisd_MAY27{system}_datasets")
            )
            datasets_by_tag = {data.tag: data for data in datasets}

            bliss_dataset, bliss_eps, bliss_info = generate_bliss_hct_dataset(
                system,
                H,
                HQ,
                cisd_gs,
                n_qubits,
                text,
            )
            datasets_by_tag[bliss_hct_tag] = bliss_dataset
            BenchmarkData.save_datasets(
                [bliss_dataset],
                output_dir / f"july08_{system}_bliss_hct_datasets",
            )

            original_fci_ent = get_entropies_at_cuts(
                fci_gs,
                n_qubits,
                log_base=log_base,
            )
            original_cisd_ent = get_entropies_at_cuts(
                cisd_gs,
                n_qubits,
                log_base=log_base,
            )
            write_entropies(text, "\nFCI entanglement before Clifford:", original_fci_ent)
            write_entropies(
                text,
                "\nCISD entanglement before Clifford (Fiedler reference):",
                original_cisd_ent,
            )

            system_result = {
                "n_qubits": n_qubits,
                "fci_energy": fci_e,
                "cisd_energy": cisd_e,
                "fci_entanglement_before_clifford": original_fci_ent,
                "cisd_entanglement_before_clifford": original_cisd_ent,
                "bliss_hct": {
                    "tag": bliss_hct_tag,
                    "epsilons": bliss_eps,
                    "info": bliss_info,
                },
                "benchmarks": {},
            }
            all_results["systems"][system] = system_result

            transform_cache = {}
            fiedler_cache = {}

            def get_transformed(dataset_tag):
                if dataset_tag in transform_cache:
                    return transform_cache[dataset_tag]

                symdata = datasets_by_tag[dataset_tag]
                ent, H_perm, clifford, fci_rot = get_permuted_bipartite_entanglement(
                    symdata.symmetries,
                    HQ,
                    n_qubits,
                    fci_energy=fci_e,
                    fci_gs=fci_gs,
                    verbose=False,
                    return_state=True,
                    return_clifford=True,
                    log_base=log_base,
                    use_dmrg=False,
                    synthesis_basis=synthesis_basis,
                    generator_mapping=generator_mapping,
                )
                cisd_rot = clifford.transform_state(cisd_gs)
                cisd_ent = get_entropies_at_cuts(cisd_rot, n_qubits, log_base=log_base)
                transformed_symmetries = list(clifford.transformed_symmetries)
                transform_cache[dataset_tag] = {
                    "symdata": symdata,
                    "ent": ent,
                    "H": H_perm,
                    "fci_state": fci_rot,
                    "state": cisd_rot,
                    "cisd_ent": cisd_ent,
                    "clifford": clifford,
                    "transformed_symmetries": transformed_symmetries,
                }
                return transform_cache[dataset_tag]

            def get_fiedler(dataset_tag):
                if dataset_tag in fiedler_cache:
                    return fiedler_cache[dataset_tag]

                transformed = get_transformed(dataset_tag)
                ent_reord, H_reord, state_reord, fiedler_info = do_fiedler_reordering(
                    transformed["H"],
                    transformed["state"],
                    n_qubits=n_qubits,
                    verbose=False,
                    log_base=log_base,
                )
                fci_state_reord = reorder_statevector_axes(
                    transformed["fci_state"],
                    fiedler_info["ordering"],
                    n_qubits,
                )
                fci_ent_reord = get_entropies_at_cuts(
                    fci_state_reord,
                    n_qubits,
                    log_base=log_base,
                )
                perm = invert_ordering(fiedler_info["ordering"])
                reordered_symmetries = [
                    permute_qubits_in_qubit_operator(sym, perm)
                    for sym in transformed["transformed_symmetries"]
                ]
                fiedler_cache[dataset_tag] = {
                    "ent": ent_reord,
                    "fci_ent": fci_ent_reord,
                    "H": H_reord,
                    "state": state_reord,
                    "fci_state": fci_state_reord,
                    "info": fiedler_info,
                    "reordered_symmetries": reordered_symmetries,
                }
                return fiedler_cache[dataset_tag]

            needed_tags = sorted(
                {
                    tag
                    for _, tag, _ in dmrg_jobs + mpo_jobs
                    if tag in datasets_by_tag
                }
            )
            for dataset_tag in needed_tags:
                transformed = get_transformed(dataset_tag)
                symdata = transformed["symdata"]

                print("\n" + "-" * 80, file=text)
                print(f"{dataset_tag}", file=text)
                print("Saved symmetries:", file=text)
                for sym in symdata.symmetries:
                    print(f"  {sym}", file=text)
                print("Symmetries after Clifford:", file=text)
                for sym in transformed["transformed_symmetries"]:
                    print(f"  {sym}", file=text)
                write_entropies(
                    text,
                    "FCI entanglement after Clifford:",
                    transformed["ent"],
                )
                write_entropies(
                    text,
                    "CISD entanglement after Clifford (Fiedler reference):",
                    transformed["cisd_ent"],
                )

                system_result["benchmarks"][dataset_tag] = {
                    "saved_symmetries": sym_strings(symdata.symmetries),
                    "symmetries_after_clifford": sym_strings(
                        transformed["transformed_symmetries"]
                    ),
                    "fci_entanglement_after_clifford": transformed["ent"],
                    "cisd_entanglement_after_clifford": transformed["cisd_ent"],
                }

            fiedler_tags = sorted(
                {
                    tag
                    for _, tag, use_fiedler in dmrg_jobs + mpo_jobs
                    if use_fiedler and tag in datasets_by_tag
                }
            )
            for dataset_tag in fiedler_tags:
                fiedler = get_fiedler(dataset_tag)
                print("\n" + "-" * 80, file=text)
                print(f"{dataset_tag} + Fiedler", file=text)
                print("Fiedler ordering:", fiedler["info"]["ordering"], file=text)
                print("Symmetries after Clifford + Fiedler:", file=text)
                for sym in fiedler["reordered_symmetries"]:
                    print(f"  {sym}", file=text)
                write_entropies(
                    text,
                    "FCI entanglement after Clifford + Fiedler:",
                    fiedler["fci_ent"],
                )
                write_entropies(
                    text,
                    "CISD entanglement after Clifford + Fiedler:",
                    fiedler["ent"],
                )

                system_result["benchmarks"][dataset_tag][
                    "fiedler_ordering"
                ] = fiedler["info"]["ordering"]
                system_result["benchmarks"][dataset_tag][
                    "symmetries_after_fiedler"
                ] = sym_strings(fiedler["reordered_symmetries"])
                system_result["benchmarks"][dataset_tag][
                    "fci_entanglement_after_fiedler"
                ] = fiedler["fci_ent"]
                system_result["benchmarks"][dataset_tag][
                    "cisd_entanglement_after_fiedler"
                ] = fiedler["ent"]
                system_result["benchmarks"][dataset_tag][
                    "fiedler_info"
                ] = fiedler["info"]

            for label, dataset_tag, use_fiedler in dmrg_jobs:
                if dataset_tag not in datasets_by_tag:
                    continue
                transformed = get_transformed(dataset_tag)
                working = get_fiedler(dataset_tag) if use_fiedler else transformed

                if args.skip_dmrg:
                    dmrg_bd = ""
                    dmrg_energy = ""
                else:
                    guess_mps = qtn.MatrixProductState.from_dense(
                        working["state"],
                        cutoff=1e-20,
                    )
                    dmrg_bd, dmrg_energy = find_dmrg_conv_bd_quimb(
                        working["H"],
                        n_qubits,
                        fci_e,
                        tol=1.6e-3,
                        n_sweeps=100,
                        reps=1,
                        verbose=False,
                        compress_cutoff=1e-20,
                        sweep_tol=1e-6,
                        noise=1e0,
                        bsz=2,
                        guess_mps=guess_mps,
                        seed=0,
                    )

                row = {
                    "system": system,
                    "benchmark": label,
                    "dataset_tag": dataset_tag,
                    "fiedler": use_fiedler,
                    "n_qubits": n_qubits,
                    "dmrg_bd": dmrg_bd,
                    "dmrg_energy": dmrg_energy,
                }
                dmrg_rows.append(row)
                print(f"DMRG {label}: {row}", file=text)
                write_csv(dmrg_csv, dmrg_rows)

            for label, dataset_tag, use_fiedler in mpo_jobs:
                if dataset_tag not in datasets_by_tag:
                    continue
                transformed = get_transformed(dataset_tag)
                working = get_fiedler(dataset_tag) if use_fiedler else transformed

                if args.skip_mpo:
                    mpo_bd = ""
                else:
                    mpo = MPO_from_QubitOperator(
                        working["H"],
                        None,
                        mpo_cutoff=1e-20,
                        compression_freq=20,
                        verbose=False,
                    )
                    mpo_bd = max(mpo.bond_sizes())

                row = {
                    "system": system,
                    "benchmark": label,
                    "dataset_tag": dataset_tag,
                    "fiedler": use_fiedler,
                    "n_qubits": n_qubits,
                    "mpo_bd": mpo_bd,
                }
                mpo_rows.append(row)
                print(f"MPO {label}: {row}", file=text)
                write_csv(mpo_csv, mpo_rows)

            runtime_rows = make_runtime_rows(dmrg_rows, mpo_rows)
            write_csv(runtime_csv, runtime_rows)
            save_json(json_file, all_results)

    runtime_rows = make_runtime_rows(dmrg_rows, mpo_rows)
    write_csv(runtime_csv, runtime_rows)
    save_json(json_file, all_results)
    print(f"Wrote text output to {text_file}")
    print(f"Wrote JSON output to {json_file}")
    print(f"Wrote DMRG CSV to {dmrg_csv}")
    print(f"Wrote MPO CSV to {mpo_csv}")
    print(f"Wrote runtime/memory CSV to {runtime_csv}")


if __name__ == "__main__":
    main()
